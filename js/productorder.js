function insert()
{
	var prod = document.getElementById('product').value;
	var prc = document.getElementById('price').value;

	var table = document.getElementById('table');

	var newrow = table.insertRow(table.rows.length);

	var cel1 = newrow.insertCell(0);
	var cel2 = newrow.insertCell(1);

	cel1.innerHTML = prod;
	cel2.innerHTML = prc;

	var table = document.getElementById("table"), sumVal=0;

	for (var i=1; i<table.rows.length; i++)
	{
		sumVal = sumVal + parseInt(table.rows[i].cells[1].innerHTML);
	}

	document.getElementById("val").innerHTML = "Subtotal " +sumVal;
	console.log(sumVal);

	var prod = String(document.getElementById("product").value);

	var showprod = document.getElementById('order');
	showprod.value=prod;

	document.getElementById("product").value="";
	document.getElementById("price").value="";

}
