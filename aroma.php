Whether you need to relax, ease muscle tension, or clear out the remnants of a cold, essential oil added to a carrier oil can help alleviate most symptoms. The next time you're traveling and pop into an upscale spa, opt for an aromatherapy massage for a special spin on the traditional rub-down. <br><br>

In an aromatherapy massage, essential oils are mixed with a carrier oil like sweet almond, jojoba, or grapeseed. Therapists will then add a couple drops of popular essential oils—like lavender, peppermint, bergamot, rose, or geranium—to the mixture they use in your massage. More sophisticated spas create their own custom blends ahead of time—using up to five oils in a mixture and naming their signature oils for their various effects. A relaxing aromatherapy massage might include oil infused with lavender or bergamot, while peppermint and eucalyptus may be used to soothe sore muscles.<br><br>

At the beginning of your treatment, the therapist will invite you to smell various blends, describing the essential oils each one contains and their effects. To narrow down your preferred blend, consider which scent resonates with you, as well as what effect you want to achieve from your massage. You can choose something that is relaxing, balancing, or invigorating, based on how you want to feel when you're done.<br><br>

After you choose the oil, your therapist will leave the room, allowing you to disrobe and lay (usually face down) on the table. An aromatherapy massage usually starts with three deep inhalations of the oil you chose, because that's the quickest way to get it into the body. These three breaths also help you relax and become present in the moment. Sometimes, therapists will start with a head massage so that you continue to smell the scent as it's being worked into your body.
