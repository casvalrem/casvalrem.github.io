Because lomi lomi was passed down in families, a number of styles of this massage exist. One of the first people to teach this massage to non-Hawaiians was Auntie Margaret Machado, who taught until her death in 2009, and many practitioners do the massage in her tradition. However, a number of variations are available, including ones based on the teachings of another early teacher, Abraham Kawai'i. Some other names you might hear are Mana Lomi, Sacred Lomi or Hawaiian Temple Bodywork.<br><br>

Traditionally, lomi lomi has an important spiritual component, and practitioners begin by saying a prayer to themselves. This massage incorporates the Hawaiian concept of aloha, which means love, unification and breath, and promotes personal harmony. Even in modern practice, lomi lomi therapists are taught to focus on the massage with love and intention. Auntie Margaret Machado is often quoted as having said that the difference between Swedish massage and lomi lomi is "loving touch."<br><br>

The physical benefits of this massage are the same as other types of massage in terms of relaxing muscles, promoting circulation and releasing blocked energy. The difference lies in the Hawaiian traditions woven into the massage, including the belief that "loving hands" help you release not only physical tension but also mental, emotional and spiritual blockages.<br><br>
