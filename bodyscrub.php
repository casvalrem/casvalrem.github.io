A body scrub is done with an abrasive material—usually sea salt or sugar—mixed with some kind of massage oil and an aromatic like essential oils. If the scrub uses salt, it might be called a salt scrub, salt glow or sea salt scrub.<br><br>

The scrub is followed by an application of high-quality lotion or cream that leaves your skin hydrated. A body scrub is not technically a massage because body treatments can be performed by estheticians, who are only licensed to work on skin, not the underlying muscle tissue (unless they are giving massage to the face, neck, and shoulders.)  <br><br>

A body scrub usually takes place in a wet room, which has a tile floor and a drain. The therapist may offer you disposable underwear, the leave the room. You will start face-down on a massage table that is covered with a towel, a sheet or a thin piece of plastic, or on a special wet table with a Vichy shower overhead. In that case, you won't have to get up to be rinsed off.<br><br>

The therapist will return and start by gently rubbing the exfoliant on your back, the backs of your arms, and the backs of your legs and feet. You may be draped with a towel so only the part he/she is working on is exposed. Then you turn over and he/she does the other side.<br><br>

When the therapist is finished, you usually step into a shower to rinse off. Be sure to rinse thoroughly so you don't take little granules back to the table. And don't use shower gel -- it's good to keep the oil and aromatics on your skin. If the spa is doing the treatment on a wet table, the therapist will either rinse you off with a hand-held shower or turn on a Vichy shower.<br><br>

If you step into the shower, the therapist will put clean sheets on the treatment table while you're showering and step out of the room again. You dry off and lie face-down on the treatment table underneath a sheet or towel. Then the therapist returns and applies body lotion or oil.
