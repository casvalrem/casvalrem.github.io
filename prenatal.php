Massage therapy during pregnancy is a wonderful complementary choice for prenatal care. It is a healthy way to reduce stress and promote overall wellness. Massage relieves many of the normal discomforts experienced during pregnancy, such as backaches, stiff neck, leg cramps, headaches and edema (or swelling).<br><br>

In addition, massage for pregnant women reduces stress on weight-bearing joints, encourages blood and lymph circulation, helps to relax nervous tension -- which aids in better sleep -- and can help relieve depression or anxiety caused by hormonal changes.<br><br>

DURING THE PRENATAL MASSAGE<br><br>
At Massage Envy, our therapists take extra care to ensure our pregnancy massage clients are comfortable. Sideline positioning with supportive, soft cushions is used to alleviate the extra strain on the lower back and pelvic areas when the pressure of massage techniques are applied. Extra strain in these areas can cause unnecessary back or abdominal pain, so naturally we want to avoid any potential discomfort.<br><br>

Through the second trimester, you may prefer to lie on your back with a small, cushioned wedge placed under one hip to provide a slight elevation. Depending on where you are in your pregnancy, your therapist may utilize such massage techniques as reflexology, Swedish massage or certain techniques that address your specific requests and needs.<br><br>

RELIEF OF EVERYDAY SYMPTOMS<br><br>
Massage for pregnant women offers a number of benefits, and it's always a good idea to discuss with your therapist any everyday symptoms you'd like to see relieved. Those might include:

Headaches can be relieved by massage focusing on the head, neck and shoulders. In addition, maintaining optimal levels of stress relief through massage reduces the chances of migraines or tension headaches by relaxing trigger points and muscle spasms.
Muscle tension created by carrying the extra weight of a baby can be relieved by encouraging blood flow to the afflicted areas. This provides more nutrient-rich oxygen and also increases the flow of lymphatic fluid, which sweeps away toxins and metabolic waste.
Fatigue, backaches, leg cramps and swelling/edema can all be relieved through various types of massage for pregnant women.<br><br>

The ordinary aches and pains of pregnancy are countered by the release of serotonin, your body's natural anti-pain chemical, which is stimulated by massage.
While a massage can't promise that your newborn will sleep through the night, it can provide you with a better night's sleep both during pregnancy and afterwards. Regular massage therapy not only helps diminish anxiety and discomfort but boosts relaxation as well. This ultimately can lead to improved sleep patterns.<br><br>

In addition, the serotonin, endorphins and dopamine released by your body in response to massage provide an extra helping of those natural chemicals. "Massage is very effective at increasing deep sleep," says Touch Research Institute director Dr. Tiffany Field.<br><br>

STAYING HEALTHY<br><br>
Women with normal, low-risk pregnancies can benefit greatly from the variety of massage techniques offered by a Massage Envy professional massage therapist. However, women in high-risk pregnancies should consult their doctor or midwife before beginning one of our massage therapy programs. If you experience discomfort at any time during the pregnancy massage, tell your therapist immediately.
